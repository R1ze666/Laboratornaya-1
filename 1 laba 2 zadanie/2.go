package main

import "fmt"

func main() {

	var myInt int = 42
	var myFloat float64 = 3.14
	var myString string = "Hello, Go!"
	var myBool bool = true

	fmt.Println("Значение переменной типа int:", myInt)
	fmt.Println("Значение переменной типа float64:", myFloat)
	fmt.Println("Значение переменной типа string:", myString)
	fmt.Println("Значение переменной типа bool:", myBool)
}
